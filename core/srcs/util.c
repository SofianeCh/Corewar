#include "corewar.h"

int		dump_memory()
{
	int		i;

	i = -1;
	while (++i < MEM_SIZE)
	{
		ft_printf("%.2x%c", g_all.arena[i], (i + 1) % 32 ? ' ' : '\n');
	}
	return (0);
}